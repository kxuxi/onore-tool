/**
 * 兵種名を報告用の短縮版に変換する。
 * `UNIT_SHORT_NAMES` の中で前方一致する最長の名前を返す。
 * 一致が無ければ元の文字列をそのまま返す（安全側）。
 *
 * オリジナル兵・`*` 始まりの整形は `normalizeDisplayToken` で済ませてから渡すこと。
 */
export function shortUnit(unit: string): string {
  const u = unit.trim();
  if (!u) return unit;
  let best = "";
  for (const name of UNIT_SHORT_NAMES) {
    if (name.length > best.length && u.startsWith(name)) {
      best = name;
    }
  }
  return best || u;
}
